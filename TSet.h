#pragma once  // Защита от повторного включения


// 



#include <iostream>   // Для ввода/вывода
#include "TBitField.h"  // Подключаем класс битового поля

// Класс TSet - реализация множества на основе битового поля
// Хранит множество целых чисел от 0 до MaxPower-1
class TSet
{
private:
    int MaxPower;      // Максимальная мощность множества (размер универсума)
    TBitField BitFild; // Битовое поле для хранения характеристического вектора
    // Если бит i = 1, то число i принадлежит множеству
    
public:
    // Конструкторы
    
    // Создает пустое множество заданной мощности
    // Например: TSet(100) создает множество для чисел 0..99
    TSet(int mp);
    
    // Конструктор копирования
    TSet(const TSet& s);
    
    // Конструктор преобразования из битового поля
    // Позволяет создать множество из готового битового поля
    TSet(const TBitField& bf);
    
    // Оператор преобразования в битовое поле
    // Позволяет использовать множество там, где ожидается битовое поле
    operator TBitField();
    
    // Методы для работы с элементами множества
    
    // Добавляет элемент в множество (устанавливает соответствующий бит)
    void InsLem(const int elem);
    
    // Удаляет элемент из множества (очищает соответствующий бит)
    void DelLem(const int elem);
    
    // Проверяет, принадлежит ли элемент множеству
    // Возвращает 1 - да, 0 - нет
    int isMember(const int elem) const;
    
    // Перегруженные операторы
    
    // Сравнение множеств на равенство
    int operator ==(const TSet& s);
    
    // Присваивание множеств
    TSet& operator =(const TSet& s);
    
    // Объединение множеств (A ? B)
    TSet operator+(const TSet& s);
    
    // Пересечение множеств (A ? B)
    TSet operator*(const TSet& s);
    
    // Дополнение множества (¬A) - все элементы, кроме тех, что в A
    TSet operator~();
    
    // Дружественные функции для ввода/вывода
    
    // Вывод множества в формате { 1, 5, 10 }
    friend std::ostream& operator<<(std::ostream& os, const TSet& s) {
        os << "{ ";  // Начинаем с фигурной скобки
        
        bool flag = true;  // Флаг для отслеживания первого элемента
        
        // Проходим по всем возможным элементам от 0 до MaxPower-1
        for (int i = 0; i < s.MaxPower; i++) {
            if (s.isMember(i)) {  // Если элемент принадлежит множеству
                if (!flag) {       // Если это не первый элемент
                    os << ", ";    // Добавляем запятую и пробел
                }
                os << i;           // Выводим сам элемент
                flag = false;      // После первого элемента сбрасываем флаг
            }
        }
        
        os << " }";  // Закрываем фигурную скобку
        return os;   // Возвращаем поток для цепочки вызовов
    }
    
    // Ввод множества (чтение чисел через пробел)
    friend std::istream& operator>>(std::istream& is, TSet& s) {
        // Сначала очищаем множество
        for (int i = 0; i < s.MaxPower; i++) {
            s.DelLem(i);  // Удаляем все элементы
        }
        
        int elem;  // Переменная для хранения введенного числа
        
        // Читаем числа, пока они есть в потоке
        while (is >> elem) {
            // Проверяем, что элемент в допустимом диапазоне
            if (elem >= 0 && elem < s.MaxPower) {
                s.InsLem(elem);  // Добавляем в множество
            }
            
            // Проверяем, не достигнут ли конец строки
            if (is.peek() == '\n') {
                is.get();  // Съедаем символ новой строки
                break;     // Выходим из цикла
            }
        }
        
        return is;  // Возвращаем поток для цепочки вызовов
    }
};